#define HEARTBEAT_ID        0x0001

#define STATE_CHANGE_CAN_ID 0x0010
#define STANDBY_CAN_ID      0x0011
#define AUTONOMOUS_CAN_ID   0x0012
#define MANUAL_CAN_ID       0x0013

#define NODE_GOOD_CAN_ID_MASK 0x0020
#define NODE_BAD_CAN_ID_MASK  0x0021

#define MOTOR_CONTROL_RPM_MASK_MANUAL 0x0040
#define MOTOR_1_RPM_MANUAL            0x0041
#define MOTOR_2_RPM_MANUAL            0x0042
#define MOTOR_3_RPM_MANUAL            0x0043

#define MOTOR_CONTROL_RPM_MASK_AUTONOMOUS 0x0080
#define MOTOR_1_RPM_AUTONOMOUS            0x0081
#define MOTOR_2_RPM_AUTONOMOUS            0x0082
#define MOTOR_3_RPM_AUTONOMOUS            0x0083

#define IS_MOTOR1_RPM_CONTROL(motor) ((motor & MOTOR_1_RPM_AUTONOMOUS) || (motor & MOTOR_1_RPM_MANUAL))
#define IS_MOTOR2_RPM_CONTROL(motor) ((motor & MOTOR_2_RPM_AUTONOMOUS) || (motor & MOTOR_2_RPM_MANUAL))
#define IS_MOTOR3_RPM_CONTROL(motor) ((motor & MOTOR_3_RPM_AUTONOMOUS) || (motor & MOTOR_3_RPM_MANUAL))

#define MOTOR_CONTROL_CURRENT_MASK_MANUAL 0x0100
#define MOTOR_1_CURRENT_MANUAL            0x0101
#define MOTOR_2_CURRENT_MANUAL            0x0102
#define MOTOR_3_CURRENT_MANUAL            0x0103

#define MOTOR_CONTROL_CURRENT_MASK_AUTONOMOUS 0x0200
#define MOTOR_1_CURRENT_AUTONOMOUS            0x0201
#define MOTOR_2_CURRENT_AUTONOMOUS            0x0202
#define MOTOR_3_CURRENT_AUTONOMOUS            0x0203

#define IS_MOTOR1_CURRENT_CONTROL(motor) ((motor & MOTOR_1_CURRENT_AUTONOMOUS) || (motor & MOTOR_1_CURRENT_MANUAL))
#define IS_MOTOR2_CURRENT_CONTROL(motor) ((motor & MOTOR_2_CURRENT_AUTONOMOUS) || (motor & MOTOR_2_CURRENT_MANUAL))
#define IS_MOTOR3_CURRENT_CONTROL(motor) ((motor & MOTOR_3_CURRENT_AUTONOMOUS) || (motor & MOTOR_3_CURRENT_MANUAL))

#define WIRELESS_ESTOP  120

#define LED_ON_TEST_MASK 0x0400
#define LED_1_TEST_ON    0x0401
#define LED_2_TEST_ON    0x0402
#define LAMP_1_TEST_ON   0x0403
#define LAMP_2_TEST_ON   0x0404
#define LAMP_3_TEST_ON   0x0405
#define LAMP_4_TEST_ON   0x0406

#define LED_OFF_TEST_MASK 0x0800
#define LED_1_TEST_OFF    0x0801
#define LED_2_TEST_OFF    0x0802
#define LAMP_1_TEST_OFF   0x0803
#define LAMP_2_TEST_OFF   0x0804
#define LAMP_3_TEST_OFF   0x0805
#define LAMP_4_TEST_OFF   0x0806

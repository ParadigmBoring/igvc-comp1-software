#define LED_1_TEST_OFF    0x0201
#define LED_2_TEST_OFF    0x0202
#define LAMP_1_TEST_OFF   0x0203
#define LAMP_2_TEST_OFF   0x0204
#define LAMP_3_TEST_OFF   0x0205
#define LAMP_4_TEST_OFF   0x0206

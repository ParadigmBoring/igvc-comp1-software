#define LED_2_TEST_ON    0x0102
#define LAMP_1_TEST_ON   0x0103
#define LAMP_2_TEST_ON   0x0104
#define LAMP_3_TEST_ON   0x0105
#define LAMP_4_TEST_ON   0x0106

